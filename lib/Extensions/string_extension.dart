extension PolishCharactersExtension on String {
  String replacePolishCharacters() {
    Map<String, String> polishCharacterMap = {
      'ą': 'a',
      'ć': 'c',
      'ę': 'e',
      'ł': 'l',
      'ń': 'n',
      'ó': 'o',
      'ś': 's',
      'ź': 'z',
      'ż': 'z',
      'Ą': 'A',
      'Ć': 'C',
      'Ę': 'E',
      'Ł': 'L',
      'Ń': 'N',
      'Ó': 'O',
      'Ś': 'S',
      'Ź': 'Z',
      'Ż': 'Z'
    };

    return this.split('').map((char) {
      return polishCharacterMap[char] ?? char;
    }).join('');
  }
}
